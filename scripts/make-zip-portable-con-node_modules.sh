#!/bin/sh
# ZIP “portatile”: include node_modules (dopo unzip npm install di solito non serve).
# ATTENZIONE: archivio grande e creazione lenta.
# Uso: dalla root del progetto →  bash scripts/make-zip-portable-con-node_modules.sh
set -e
STAMP="$(date +%Y%m%d-%H%M)"
PROJECT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
OUT="$(dirname "$PROJECT_DIR")/progetto-PORTATILE-node_modules-${STAMP}.zip"
PARENT="$(dirname "$PROJECT_DIR")"
NAME="$(basename "$PROJECT_DIR")"

echo ">>> npm install..."
(cd "$PROJECT_DIR" && npm install)

echo ">>> Creazione ZIP (include node_modules, esclude solo .git)..."
cd "$PARENT"
zip -r "$OUT" "$NAME" \
  -x "${NAME}/.git/*" \
  -x "${NAME}/.git/**"

echo "Creato: $OUT"
ls -lh "$OUT"
